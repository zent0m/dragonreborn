    /* animation */
    void animate_luna(void) {
        /* jump */
        if (isJumping || !showedJump) {
            // Is jumping, clear the bottom line and tell animation to render 1 line up
            oled_set_cursor(LUNA_X, LUNA_Y + 3);
            oled_write("     ", false);
            jump_height = 1;
            showedJump  = true;
        } else {
            // Not jumping, clears the top row and tells animation to render bottom 3 rows
            oled_set_cursor(LUNA_X, LUNA_Y);
            oled_write("     ", false);
            jump_height = 0;
        }

        /* switch frame */
        // current_luna_frame = (current_luna_frame + <number of lines per image>) % <lines x how many frames>;
        current_luna_frame = (current_luna_frame + 3) % 6;

        /* current status */
        if (host_keyboard_led_state().caps_lock) {
            oled_render_image(3, LUNA_SIZE, bark, LUNA_X, LUNA_Y + 1, current_luna_frame);

        } else if (isSneaking) {
            oled_render_image(3, LUNA_SIZE, sneak, LUNA_X, LUNA_Y + 1 - jump_height, current_luna_frame);

        } else if (prev_wpm <= MIN_WALK_SPEED) {
            oled_render_image(3, LUNA_SIZE, sit, LUNA_X, LUNA_Y + 1, current_luna_frame);

        } else if (prev_wpm <= MIN_RUN_SPEED) {
            oled_render_image(3, LUNA_SIZE, walk, LUNA_X, LUNA_Y + 1 - jump_height, current_luna_frame);

        } else {
            oled_render_image(3, LUNA_SIZE, run, LUNA_X, LUNA_Y + 1 - jump_height, current_luna_frame);
        }
    }

    if (keystroke > OLED_TIMEOUT) {
        oled_off();
    } else if (timer_elapsed(luna_timer) > (LUNA_FRAME_DURATION - (prev_wpm / 2))) {
        luna_timer = timer_read();
        animate_luna();
    }

